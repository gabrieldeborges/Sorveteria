    private void btn_cadastrarCadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_cadastrarCadActionPerformed
        String sexo = null, senha, cSenha, cod; 
        
        Connection conexao =  ConexaoDB.abrirConexao();        
        CadastroBean cadB = new CadastroBean();        
        CadastroDAO cadD = new CadastroDAO(conexao);
        
        senha       =   txt_senhaCad.getText();
        cSenha     = txt_confirmarCad.getText();    
        if(senha==cSenha)
        {
            cadB.setNome(txt_nomeCad.getText());
            cadB.setCpf(txt_cpfCad.getText());
            cadB.setEmail(txt_emailCad.getText());
            cadB.setCidade(cbx_cidades.getSelectedItem().toString());
            cadB.setEndereco(txt_enderecoCad.getText());
            cadB.setSenha(txt_senhaCad.getName());
            cod=txt_codfunc.getText();            
            if(rad_sFemi.isSelected())
            {
                sexo = "feminino";
            }
            else if (rad_sMasc.isSelected())
            {
                sexo = "masculino";
            }
            cadB.setSexo(sexo);
            lbl_status.setText(cadD.inserirCliente(cadB));      
            
            lbl_status.setVisible(true);
        }
    }//GEN-LAST:event_btn_cadastrarCadActionPerformed

